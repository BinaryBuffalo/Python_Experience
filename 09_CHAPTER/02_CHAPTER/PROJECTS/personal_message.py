# Prints a personal message to the user & then converts the message to uppercase lowercase & title

person_name = "Joseph"
print(f"hello,\t {person_name} Would you like to learn python?")
print(f"Name1 -> {person_name.lower()}")
print(f"Name2 -> {person_name.upper()}")
print(f"Name3 -> {person_name.title()}")
